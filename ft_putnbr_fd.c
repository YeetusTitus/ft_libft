#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0 && !(n == -2147483648))
	{
		n *= -1;
		ft_putchar_fd('-', fd);
	}
	if (n == -2147483648)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd('8', fd);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (!(n == -2147483648))
	{
		n += '0';
		ft_putchar_fd(n, fd);
	}
}
